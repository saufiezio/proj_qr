  Future<List> register() async {
    String name = nama.text;
    String emails = email.text;
    String phones = phone.text;
    String passwords = pass.text;
    String confirms = confirmPassword.text;

    if (name.isNotEmpty == true &&
        emails.isNotEmpty == true &&
        phones.isNotEmpty == true &&
        passwords.isNotEmpty == true &&
        confirms.isNotEmpty == true &&
        confirms == passwords) {
      final response = await http
          .post("https://qniti.com/myqniti/apps/registerIOS.php", body: {
        "username": nama.text,
        "userphone": phone.text,
        "useremail": email.text,
        "userpass": pass.text,
      });

      print(response.body);
      if (response.body == 'Success') {
        saveName();
      } else if (response.body == 'Exist') {
        Toast.show("User with that email already exist", context,
            duration: Toast.LENGTH_LONG, gravity: Toast.CENTER);
      }
    } else {
      Toast.show(
          "Registration fail, fill all the field and check your password",
          context,
          duration: Toast.LENGTH_LONG,
          gravity: Toast.CENTER);

//
    }
  }

